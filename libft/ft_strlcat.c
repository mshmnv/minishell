#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	res;

	i = ft_strlen(dest);
	res = ft_strlen(src);
	if (size < i)
		res += size;
	else
		res += i;
	if (size != 0)
	{
		j = 0;
		while (src[j] && i < size - 1)
		{
			dest[i] = src[j];
			i++;
			j++;
		}
		dest[i] = '\0';
	}
	return (res);
}
